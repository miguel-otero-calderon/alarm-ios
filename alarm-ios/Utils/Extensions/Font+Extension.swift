import Foundation
import SwiftUI

extension Font {
    static func sourceSansProLight(size: CGFloat) -> Font {
        Font.custom("SourceSansPro-Light", size: size)
    }

    static func sourceSansProSemiboldIt(size: CGFloat) -> Font {
        Font.custom("SourceSansPro-SemiboldIt", size: size)
    }

    static func sourceSansProBold(size: CGFloat) -> Font {
        Font.custom("SourceSansPro-Bold", size: size)
    }

    static func sourceSansProRegular(size: CGFloat) -> Font {
        Font.custom("SourceSansPro-Regular", size: size)
    }

    static func sourceSansProSemibold(size: CGFloat) -> Font {
        Font.custom("SourceSansPro-Semibold", size: size)
    }
}

